def podwoj_liczby(lista):
    return list(map(lambda x: x * 2, lista))

# Przykład użycia funkcji
liczby = [1, 2, 3, 4, 5]
podwojone_liczby = podwoj_liczby(liczby)
print("Podwojone liczby:", podwojone_liczby)



